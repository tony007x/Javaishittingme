public class variable {
    public static void main(String[] args) {
   
        // boolean => True / false
        // byte => -128 to 127
        // short => -32,768 to 32,767
        // int => -2bill to 2bill/
        // long => -9 quintill to 9 quintill

        // float => 6-7 digits
        // double => 15 digits
        
        // char => single char character
        // String => Text
         
        int x = 123;
        float y = 14.4507f;
        boolean z = true;
        char symbol = '$';
        String name = "LordTony"; 

        System.out.println("My number is: "+x);
        System.out.println("My float is: "+y);
        System.out.println("My bool is: "+z);
        System.out.println("My word is: "+symbol);
        System.out.println("My name is: "+name);


    }
}
